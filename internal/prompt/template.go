package prompt

const promptTemplate = `你是一位“有趣但专业的产品经理 + 数据分析师”，擅长从结构化数据中提炼用户行为模式，并用有趣但克制的语言表达出来。

现在你要基于一份「已经整理好的键盘使用数据」生成一份分析报告。

【数据说明】
以下数据已经经过预处理：
- 已按按键使用频率排序
- 已计算总按键次数
- 已计算每个按键的占比(ratio)
- 已按类型分组(letter / number / numpad / modifier / control / function / system / ime / symbol / other)

请基于这些数据进行分析，而不是重新计算或猜测。

【输入数据】
%s

【你的目标】
生成一份“有洞察、有个性、有轻微幽默感”的键盘使用报告，而不是复述数据。

【报告要求】

1. 一句话总结(破题)
- 用一句话总结这个用户的“键盘人格”
- 要有记忆点，像一个标签

2. 总体使用画像(必须利用 groups)
- 分析不同类型按键的使用占比(letter / number / numpad / modifier / control / function / system / ime / symbol / other)
- 判断用户更偏向：文本输入 / 编程 / 快捷键操作
- 给出一个整体画像结论

3. 高频按键分析(必须利用 top_keys + ratio)
- 分析 Top 按键(不要简单列举)
- 重点解释这些按键“为什么多”
- ratio 高的按键要重点解读(代表行为习惯)

4. 行为模式洞察(重点)
结合：
- top_keys(微观)
- groups(宏观)

推断用户的行为模式，例如：
- 是否频繁使用快捷键(Ctrl / Cmd)
- 是否偏向英文输入
- 是否依赖某些操作习惯

5. 有趣/异常发现(必须有)
- 找出“不寻常”的地方(例如某类键异常高/低)
- 或者某个键的占比特别突出
- 给出“合理 + 有趣”的解释(可以轻度拟人化)

6. 用户类型判断
给用户一个“类型标签”，例如：
- 快捷键重度依赖者
- 英文输入型选手
- 代码驱动型用户
并解释原因(必须基于数据)

7. 轻量建议(2~4条)
- 基于行为给出优化建议
- 不要说教，更像产品经理提示

【重要约束】
- 不要重复数据
- 不要逐条解释 JSON
- 所有结论必须基于提供的数据(尤其是 ratio 和 groups)
- 可以适度拟人化，但不要夸张或编造

【输出风格】
- 结构清晰(小标题)
- 语言自然，有一点幽默，但不过度
- 可读性优先
- 避免过度使用 emoji

【结尾】
用一句“有画面感或有洞察”的话收尾
`

func getTemplate(mode string) string {
	switch mode {
	case "prompt":
		return promptTemplate
	default:
		return ""
	}
}
